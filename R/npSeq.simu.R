npSeq.Simu.Data <- function(dat, seed=10)
{
	cat("generate data...")
	set.seed(seed)
	
	### set default values
	if (is.null(dat$NGENE))
	{
		dat$NGENE <- 20000
	}

	if (is.null(dat$psig))
	{
		dat$psig <- 0.3
	}
	
	if (is.null(dat$up.perc))
	{
		dat$up.perc <- 0.8
	}
	
	# simulate the data
	dat <- Simu.Mean(dat)
	dat <- Simu.Count(dat)
	dat <- Simu.Finalize(dat)
	
	# randomize the rows
	sam <- sample(1 : dat$NGENE)
	dat$n <- dat$n[sam, ]
	dat$delta <- dat$delta[sam]
	dat$rmean <- dat$rmean[sam]
	dat$mu <- dat$mu[sam, ]
	
	return(dat)
}

############################################################
#		Simulate the mean matrix
############################################################
Simu.Mean <- function(dat)
{
	T.NSAM <- sum(dat$NSAM)
	K <- length(dat$NSAM)
	
	dat$rmean <- sample(rep(Get.Rmean(), 2))
	dat$cmean <- Get.Cmean()[1 : T.NSAM]
	
	dat$mu <- dat$rmean %*% t(dat$cmean)

	# output
	dat$y <- NULL
	if (K == 1)
	{
		if (dat$type == 'survi')
		{
			cat("survi data...\n")
		} else if (dat$type == 'quant')
		{
			cat("quant data...\n")
		} else
		{
			stop("Length of NSAM is 1, but the data is not survi or quant!")
		}
		dat$y <- runif(T.NSAM, min=-1, max=1)
	} else
	{	
		if (K == 2 & dat$type == 'twoclass')
		{
			cat("twoclass data...\n")
		} else if (K > 2 & dat$type == 'multiclass')
		{
			cat("multiclass data...\n")
		} else
		{
			stop("Length of NSAM is larger than 1, but the data is not twoclass or multiclass!")
		}
		for (k in 1 : length(dat$NSAM))
		{
			dat$y <- c(dat$y, rep(k, dat$NSAM[k]))
		}
	}
	
	# simulate the survival time
	dat$gamma <- NULL
	if (dat$type == 'survi')
	{
		t <- dat$y + 1 + runif(T.NSAM, 0, 0.2)
		trunc.t <- runif(T.NSAM, 1, 2)
		dat$ty <- floor(pmin(t, trunc.t) * 20)
		dat$gamma <- as.numeric(trunc.t > t)
	}

	# differences
	nsig.up <- round(length(dat$rmean) * dat$psig * dat$up.perc)
	nsig.dn <- round(length(dat$rmean) * dat$psig * (1 - dat$up.perc))
	nsig.sm <- length(dat$rmean) - nsig.up - nsig.dn
	dat$fold.change <- NULL

	# generate differential expression
	if (K == 1)
	{
		dat$fold.change <- c(abs(rnorm(nsig.up)), -abs(rnorm(nsig.dn)), 
			rep(0, nsig.sm))
		dat$mu <- dat$mu * exp(dat$fold.change %*% t(dat$y))
	} else if (K == 2)
	{
		dat$fold.change <- c(abs(rnorm(nsig.up)), -abs(rnorm(nsig.dn)), 
			rep(0, nsig.sm))
		dat$mu[, dat$y==2] <- dat$mu[, dat$y==2] * exp(dat$fold.change)
	} else
	{
		dat$fold.change <- matrix(0, length(dat$rmean), K - 1)
		dat$fold.change[, 1] <- c(abs(rnorm(nsig.up)), -abs(rnorm(nsig.dn)), 
			rep(0, nsig.sm))
		dat$mu[, dat$y==2] <- dat$mu[, dat$y==2] * exp(dat$fold.change[, 1])
		for (k in 3 : K)
		{
			dat$fold.change[, k - 1] <- c(rnorm(nsig.up + nsig.dn), rep(0, nsig.sm))
			dat$mu[, dat$y==k] <- dat$mu[, dat$y==k] * exp(dat$fold.change[, k - 1])
		}
	}
	
	# also returns number of up, down and same
	dat$nsig.up <- nsig.up
	dat$nsig.dn <- nsig.dn
	dat$nsig.sm <- nsig.sm

	# the outcome of survival data is the survival time
	if (dat$type == 'survi')
	{
		dat$ry <- dat$y
		dat$y <- dat$ty
	}
	
	return(dat)
}

############################################################
#		Simulate the counts
############################################################
Simu.Count <- function(dat)
{
	dat <- switch(dat$option,
		Simu.Pois(dat),
		Simu.NB(dat),
		Simu.Noise(dat, pois=T),
		Simu.Noise(dat, pois=F)
	)

	return(dat)
}

############################################################
#		Finalize the simulation
#	delete the ones with too small counts,
#	and make the final number of genes exact.
############################################################
Simu.Finalize <- function(dat)
{	
	# get the keeps
	keep <- (rowSums(dat$n) > floor(length(dat$y) * 0.5))
	keep.up <- keep & c(rep(T, dat$nsig.up), rep(F, dat$nsig.dn + dat$nsig.sm))
	keep.dn <- keep & c(rep(F, dat$nsig.up), rep(T, dat$nsig.dn), 
		rep(F, dat$nsig.sm))
	keep.sm <- keep & c(rep(F, dat$nsig.up + dat$nsig.dn), rep(T, dat$nsig.sm))
	
	dat$nsig.up <- NULL
	dat$nsig.dn <- NULL
	dat$nsig.sm <- NULL
	
	# renew the data
	nsig.up <- round(dat$NGENE * dat$psig * dat$up.perc)
	nsig.dn <- round(dat$NGENE * dat$psig * (1 - dat$up.perc))
	nsig.sm <- dat$NGENE - nsig.up - nsig.dn
	
	if (sum(keep.up) < nsig.up | sum(keep.dn) < nsig.dn | sum(keep.sm) < nsig.sm)
	{
		print ("ERROR: Insufficient number of genes!!!!!!!!!!\n")
	}
	
	keep.up <- ((1 : length(dat$rmean))[keep.up])[1 : nsig.up]
	keep.dn <- ((1 : length(dat$rmean))[keep.dn])[1 : nsig.dn]
	keep.sm <- ((1 : length(dat$rmean))[keep.sm])[1 : nsig.sm]
	keep <- c(keep.up, keep.dn, keep.sm)
	
	# set delta
	dat$delta <- rep(F, dat$NGENE)
	dat$delta[1 : (nsig.up + nsig.dn)] <- T
	
	# only keep the exact number of genes
	dat$mu <- dat$mu[keep, ]
	dat$n <- dat$n[keep, ]
	dat$rmean <- dat$rmean[keep]
	
	if (length(dat$NSAM) < 3)
	{
		dat$fold.change <- dat$fold.change[keep]
	} else
	{
		dat$fold.change <- dat$fold.change[keep, ]
	}
	
	return(dat)
}

############################################################
#		Simulate Poisson counts
############################################################
Simu.Pois <- function(dat)
{
	cat("Poisson data...\n")
	
	dat$n <- matrix(rpois(length(dat$mu), dat$mu), nrow(dat$mu), ncol(dat$mu))
	
	return(dat)
}

############################################################
#		Simulate Negative Binomial counts
############################################################
Simu.NB <- function(dat)
{
	cat("NB data...\n")
	
	dat$n <- matrix(rnbinom(n=length(dat$mu), mu=dat$mu, size=1.0/0.25),
			nrow(dat$mu), ncol(dat$mu))
			
	return(dat)
}

############################################################
#		Simulate Poisson data with noise
############################################################
Simu.Noise <- function(dat, pois)
{
	cat(paste(switch(pois+1, 'NB', 'Poisson'), " data with noise...\n", sep=''))

	noise.prob <- 0.01
	noise.fold <- 10
			
	# add noise
	nflag <- matrix(runif(nrow(dat$mu) * ncol(dat$mu)) < noise.prob, 
		nrow(dat$mu), ncol(dat$mu))
	dat$mu[nflag] <- dat$mu[nflag] * noise.fold
	
	if (pois)
	{
		dat$n <- matrix(rpois(length(dat$mu), dat$mu), nrow(dat$mu), ncol(dat$mu))
	} else
	{
		dat$n <- matrix(rnbinom(n=length(dat$mu), mu=dat$mu, size=1.0/0.25),
			nrow(dat$mu), ncol(dat$mu))
	}
	
	return(dat)
}

